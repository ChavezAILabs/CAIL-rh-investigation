def is_zero_vec(v, tol=1e-8):
    return max(abs(x) for x in v) < tol

def find_in_closure(v, tol=1e-8):
    """Check if v is in span of closure vectors; return (label, sign, exact_match)."""
    if is_zero_vec(v, tol):
        return None, None, True, "zero"
    n = vec_norm(v)
    if n < tol:
        return None, None, True, "zero"
    v_dir = [x / n for x in v]
    for i, (label, cv) in enumerate(zip(CLOSURE_LABELS, CLOSURE_VECS)):
        c_norm = vec_norm(cv)
        if c_norm < tol:
            continue
        cv_dir = [x / c_norm for x in cv]
        dot = sum(a*b for a, b in zip(v_dir, cv_dir))
        if abs(dot - 1.0) < tol:
            return i, +1, True, f"+{label} (scale={n:.4f})"
        if abs(dot + 1.0) < tol:
            return i, -1, True, f"-{label} (scale={n:.4f})"
    # Not a multiple of a closure vector; check if it's a sum
    return None, None, False, "ESCAPE"

def safe(x):
    if x is None:
        return None
    if isinstance(x, (list, tuple)):
        return [safe(i) for i in x]
    if isinstance(x, np.ndarray):
        return x.tolist()
    if isinstance(x, (np.bool_,)):
        return bool(x)
    if isinstance(x, (np.integer,)):
        return int(x)
    if isinstance(x, (np.floating,)):
        if math.isnan(x) or math.isinf(x):
            return None
        return float(x)
    if isinstance(x, float):
        if math.isnan(x) or math.isinf(x):
            return None
    return x

