CLOSURE_LABELS = list(CLOSURE_VECS_16D.keys())
CLOSURE_VECS   = [CLOSURE_VECS_16D[k] for k in CLOSURE_LABELS]
N_CLOSURE = len(CLOSURE_VECS)

def norm2(v):
    return sum(x*x for x in v)

def dot16(a, b):
    return sum(x*y for x, y in zip(a, b))

def vec_norm(v):
    return math.sqrt(norm2(v))

# Verify norms
print("=== Verifying 12-vector closure norms ===")
for label, vec in zip(CLOSURE_LABELS, CLOSURE_VECS):
    n2 = norm2(vec)
    print(f"  {label:15s}: norm^2 = {n2:.6f}")

# q3 unnormalized (for the left-multiplication generating element)
# The handoff says q3 = e6 + e9 (unnormalized, norm^2 = 2).
