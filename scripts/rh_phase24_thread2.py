# ── Sedenion multiplication ──────────────────────────────────────────────────

def cd_conj(v):
    return [v[0]] + [-x for x in v[1:]]

def cd_mul(a, b):
    n = len(a)
    if n == 1:
        return [a[0] * b[0]]
    h = n // 2
    a1, a2, b1, b2 = a[:h], a[h:], b[:h], b[h:]
    c1 = [x - y for x, y in zip(cd_mul(a1, b1), cd_mul(cd_conj(b2), a2))]
    c2 = [x + y for x, y in zip(cd_mul(b2, a1), cd_mul(a2, cd_conj(b1)))]
    return c1 + c2

def sed_mul(a, b):
    """Multiply two 16D sedenion vectors (lists or arrays)."""
    return cd_mul(list(a), list(b))

# ── 12-vector closure of {q2, q3, q4} — Phase 23T1 ─────────────────────────

sqrt2 = math.sqrt(2.0)

def make_16d(positions_values):
    """Create a 16D sedenion vector from {position: value} dict."""
    v = [0.0] * 16
    for pos, val in positions_values.items():
        v[pos] = val
    return v

